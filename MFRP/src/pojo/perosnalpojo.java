package pojo;

public class perosnalpojo {
	private String annual_income;
	private String company_name;
	private String designation;
	private String total_exp;
	private String current_experience;

	public String getAnnual_income() {
		return annual_income;
	}

	public void setAnnual_income(String annual_income) {
		this.annual_income = annual_income;
	}

	public String getCompany_name() {
		return company_name;
	}

	public void setCompany_name(String company_name) {
		this.company_name = company_name;
	}

	public String getDesignation() {
		return designation;
	}

	public void setDesignation(String designation) {
		this.designation = designation;
	}

	public String getTotal_exp() {
		return total_exp;
	}

	public void setTotal_exp(String total_exp) {
		this.total_exp = total_exp;
	}

	public String getCurrent_experience() {
		return current_experience;
	}

	public void setCurrent_experience(String current_experience) {
		this.current_experience = current_experience;
	}

}
